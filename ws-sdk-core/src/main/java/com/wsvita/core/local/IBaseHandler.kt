package com.wsvita.core.local

/**
 * 【全系统通用基础处理器接口】
 * * 作用：定义应用矩阵中所有功能模块（Account, Startup, Log, Push 等）的通用接入标准。
 * 地位：作为全局最基础的 Handler 定义，未来所有独立组件、业务逻辑链条都将依赖此契约。
 * * 设计理念：
 * 1. 身份声明：每个处理器必须明确其所属的业务范围（Scope）。
 * 2. 逻辑解耦：调度器只认 Scope，不认具体实现类，从而实现跨组件的动态调用。
 *
 * create by Eastevil at 2026/4/13 16:49
 * @author Eastevil
 */
interface IBaseHandler {

    /**
     * 获取当前处理器声明的业务范围标签（Scope）。
     * * 【接入规范】：
     * 1. 业务一致性：实现类返回的字符串必须与所属组件定义的常量池保持一致。
     * 2. 调用逻辑：当分发中心发出的 ActionTag 与此返回值匹配（或为全量标识）时，该处理器将被触发。
     * 3. 扩展说明：若所属组件未定义相关常量，或分发了不支持的标签，此处理器将自动被系统跳过。
     * * @return 业务范围标识字符串（由各业务组件自行定义常量池）
     */
    fun getScope() : String;
}
